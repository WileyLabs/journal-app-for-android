package com.wiley.android.journalApp.widget;

import android.content.Context;
import android.support.v4.view.ViewPager;
import android.util.AttributeSet;
import android.view.MotionEvent;

/**
 * Created by taraskreknin on 01.09.14.
 */
public class CustomViewPager extends ViewPager {

    private boolean touchesEnabled = true;

    public CustomViewPager(Context context) {
        super(context);
    }

    public CustomViewPager(Context context, AttributeSet attrs) {
        super(context, attrs);
    }

    public void setTouchesEnabled(boolean enabled) {
        this.touchesEnabled = enabled;
    }

    @Override
    public boolean dispatchTouchEvent(MotionEvent ev) {
        return touchesEnabled && super.dispatchTouchEvent(ev);
    }
}
